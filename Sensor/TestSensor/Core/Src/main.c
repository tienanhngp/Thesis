		Temp_byte2 = DS18B20_Read();
		TEMP = (Temp_byte2<<8)|Temp_byte1;
		Temperature = (float)TEMP/16;
		buffer_int =  Convert_Fl_To_Int(Temperature) + Soil_Moisture*1000;
		sprintf(buffer, "%d", buffer_int);
